public class demo_3 {
    public static void main(String[] args) {
        int a = 10, b = 30;
        System.out.println("Relational operators");
        System.out.println("Value of a is:" + a);
        System.out.println("Value of b is:" + b);
        System.out.println("a<b is: " + (a < b));
        System.out.println("a<=b is: " + (a <= b));
        System.out.println("a>b is: " + (a > b));
        System.out.println("a>=b is: " + (a >= b));
        System.out.println("a==b is: " + (a == b));
        System.out.println("a!=b is: " + (a != b));
    }
}
